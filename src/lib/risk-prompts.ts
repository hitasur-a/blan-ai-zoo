export function buildSystemPrompt(opt: RunOptions): string {
  return opt.mode === "explanation" ? EXPLANATION_BASE : CONTRACT_BASE;
}

export function buildUserPrompt(opt: RunOptions, documentText: string): string {
  const perspectiveLabel = {
    buyer: opt.mode === "explanation" ? "買主・借主目線" : "買い手・受託者・借り手目線",
    seller: opt.mode === "explanation" ? "売主・貸主目線" : "売り手・委託者・貸し手目線",
    neutral: "中立目線",
  }[opt.perspective];

  const propertyLabel = opt.propertyType
    ? {
        "residential-buy": "住居用 (売買)",
        "residential-rent": "住居用 (賃貸)",
        commercial: "事業用",
      }[opt.propertyType]
    : null;

  const meta = [
    `# 立場 (厳守): ${perspectiveLabel}`,
    propertyLabel ? `# 物件種別: ${propertyLabel}` : null,
    `# 書類本文 (以下を分析対象とする)`,
  ]
    .filter(Boolean)
    .join("\n");

  return `${meta}\n\n${documentText.trim()}`;
}
